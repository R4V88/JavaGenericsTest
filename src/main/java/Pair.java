public class Pair <Left, Right> {

    private Left leftContent;
    private Right rightContent;

    public Left getLeftContent() {
        return leftContent;
    }

    public void setLeftContent(Left leftContent) {
        this.leftContent = leftContent;
    }

    public Right getRightContent() {
        return rightContent;
    }

    public void setRightContent(Right rightContent) {
        this.rightContent = rightContent;
    }
}
